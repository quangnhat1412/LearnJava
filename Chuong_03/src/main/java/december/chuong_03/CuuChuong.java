// in ra bang cuu chuong 7
public class CuuChuong {
    public static void main(String[] args) {
        int i = 1;
        final int CUU_CHUONG_7 = 7;
        while(i<=10){
            System.out.printf("%d x %2d = %2d\n",CUU_CHUONG_7,i,i*CUU_CHUONG_7);
            i++;
        }
    }
}
